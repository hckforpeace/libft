#include "libft.h"

char	*ft_strncpy(char *dest, const char *src, size_t n)
{
	while (*src && n > 0)
	{
		*dest++ = *src++;
		n--;
	}
	*dest = '\0';
	while (n > 0)
	{
		*dest++ = '\0';
		n--;
	}
	return (dest);
}
